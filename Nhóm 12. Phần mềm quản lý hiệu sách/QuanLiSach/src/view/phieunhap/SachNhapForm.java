    private void timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timkiemActionPerformed
        // TODO add your handling code here:
        String masach=JOptionPane.showInputDialog(this,"Mời bạn nhập mã sách");
         quanliphieunhap ql =new quanliphieunhap();
        try {
            if(ql.check_masach(masach)==false)
            {
                ArrayList<nhapmasach> nms = ql.timkiem_masach(masach);
               
                DefaultTableModel nhapsach =(DefaultTableModel) bang_nhap_sach.getModel();
                resetTable();
                for(nhapmasach a :nms)
                {
                    Object b[]={a.getMasach(),a.getTensachnhap(),a.getNxb(),a.getTacgia()};
                    nhapsach.addRow(b);
                    
                }
                
            
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Mã sách này không có trong hệ thống");
            }
        } catch (SQLException ex) {
            Logger.getLogger(SachNhapForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_timkiemActionPerformed

