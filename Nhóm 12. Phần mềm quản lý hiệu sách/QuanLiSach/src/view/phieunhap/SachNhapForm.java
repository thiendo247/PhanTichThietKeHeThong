    public void laydulieuvaobang() throws SQLException
    {
        quanliphieunhap ql =new quanliphieunhap();
        ArrayList<nhapmasach> nms =ql.get_masach();
        DefaultTableModel nhapsach =(DefaultTableModel) bang_nhap_sach.getModel();
        for(nhapmasach a :nms)
        {
            Object b[]={a.getMasach(),a.getTensachnhap(),a.getNxb(),a.getTacgia()};
            nhapsach.addRow(b);
            
        }
        
        
        
    
    }
